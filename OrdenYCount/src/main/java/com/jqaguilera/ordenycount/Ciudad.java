package com.jqaguilera.ordenycount;

/**
 *
 * @author jqagu
 */
public class Ciudad {
    
    String nombre;
    int cantidad;

    public Ciudad() {
    }

    public Ciudad(String nombre, int cantidad) {
        this.nombre = nombre;
        this.cantidad = cantidad;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public int getCantidad() {
        return cantidad;
    }

    public void setCantidad(int cantidad) {
        this.cantidad = cantidad;
    }
    
    
    
}
